#ifndef __TYPINT_H
#define __TYPINT_H

#ifndef uint8_t
typedef unsigned char uint8_t;
#endif

#ifndef uint16_t
typedef unsigned int uint16_t;
#endif

#ifndef uint32_t
typedef unsigned long uint32_t;
#endif

#ifndef int8_t
typedef signed char int8_t;
#endif

#ifndef int16_t
typedef signed int int16_t;
#endif

#ifndef int32_t
typedef signed long int32_t;
#endif

#endif

